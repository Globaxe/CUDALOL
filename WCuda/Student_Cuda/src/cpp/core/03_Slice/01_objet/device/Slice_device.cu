/**
 * output : void required !!
 */
__global__ void slice(float* ptrDevTab, int n)
    {
    const double DX = 1. / n;

    float xi=0;
    // pattern entrelacement
    const int NB_THREAD = Indice2D::nbThread();

    const int TID = Indice2D::tid();
    int s = TID;
    while (s < n)
	{
	xi = s * DX;
	ptrDevTab[s] =  4 / (1 + xi * xi);
	s += NB_THREAD;
	}
    }

